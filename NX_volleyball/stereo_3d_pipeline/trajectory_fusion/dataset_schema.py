@dataclass
class LegacySequence:
    """One track from the current TrajectoryRecorder CSV."""

    track_id: int
    rows: List[Dict[str, float]]
    metadata: Dict[str, Any] = field(default_factory=dict)

    @property
    def length(self) -> int:
        return len(self.rows)


